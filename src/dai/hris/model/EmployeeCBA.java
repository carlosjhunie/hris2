package dai.hris.model;

public class EmployeeCBA {
	private int cbaId;
	private int empId;
	private int performanceYear;
	private String myInputs;
	private String remarks;
	public int getCbaId() {
		return cbaId;
	}
	public void setCbaId(int cbaId) {
		this.cbaId = cbaId;
	}
	public int getEmpId() {
		return empId;
	}
	public void setEmpId(int empId) {
		this.empId = empId;
	}
	public int getPerformanceYear() {
		return performanceYear;
	}
	public void setPerformanceYear(int performanceYear) {
		this.performanceYear = performanceYear;
	}
	public String getMyInputs() {
		return myInputs;
	}
	public void setMyInputs(String myInputs) {
		this.myInputs = myInputs;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	
	
}
